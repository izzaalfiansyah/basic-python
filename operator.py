value1 = int(input("Masukkan Angka 1: "))
value2 = int(input("Masukkan Angka 2: "))

print("==================================")
print("Hasil Penjumlahan = " + str(value1 + value2))
print("Hasil Pengurangan = " + str(value1 - value2))
print("Hasil Perkalian   = " + str(value1 * value2))
